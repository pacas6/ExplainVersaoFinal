function showLoginCard(){

    document.getElementById('BackGroundCard').style.visibility = 'visible'
    document.getElementById('LoginCard').style.visibility = 'visible'
    
}

function showRegisterCard(){

    document.getElementById('BackGroundCard').style.visibility = 'visible'
    document.getElementById('RegisterCard').style.visibility = 'visible'

}

function hideCardAndBackGround(){

    document.getElementById('BackGroundCard').style.visibility = 'hidden'

    document.getElementById('LoginCard').style.visibility = 'hidden'
    document.getElementById('RegisterCard').style.visibility = 'hidden'

}
